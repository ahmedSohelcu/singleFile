

//-----------------------------------------
      Basic command
//-----------------------------------------
1.Oper terminal =>  CTR + ALT + T
2.Close terminal => CTR + D or CTR + SHIFT + W .
3.To know only the system name => uname
4.To view your network hostname => uname -n
5.To get information about kernel-version => uname -v
6.To print your machine hardware name => uname -m
7.All this information can be printed at once  => uname -a

//-----------------------------------------
      Linux command
//-----------------------------------------
1.Show username => whoami
2.Show system and kernal => uname -a
3.Repeat last command => !!
4.Show environmen variable => env
5.Show current directory => pwd
6.List files => ls
7. Show all including hiddent files => ls -a
            -R (Recursive list)
            -r (Reverse order)
            -S (Sort by file size)
            -1 (One file per line)
