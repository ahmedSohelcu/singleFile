//To Export big mysql table file using command line
===============================================================

        **1   F:\xampp\mysql\bin>mysqldump -u root wf floor>D:/floorNewName.sql
                    1. wf    -> database name
                    2. floor -> our table name which we want to export
                    3. D:    -> driver where we want to save exported table
                    4. floorNewName    ->exported file will save as floorNewName
